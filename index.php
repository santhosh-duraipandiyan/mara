<p class="single">Mara CMS is a flatfile or file based website editing platform. If you&#39;ve tried file based systems in the past and been disappointed with their &#39;bulletin board code&#39; page editing mechanisms, then you will be pleasantly surprised to see that Mara integrates CKEditor, arguably the most popular and powerful in-browser editing package. The same editing platform used by the majority of database driven CMS products. So, you no longer have to accept clunky text entry arrangements to go file based.&nbsp; Being file based means no database, and hence no complex setup procedure or code injection hacking risks. Oh, and we use HTML5 and CSS3, the latest and best markup languages, while many big-box competitors are still using old fashioned XHTML4.</p>

<p class="single">OK, so why go file based, anwyay? File based CMS are typically smaller, faster and simpler to manage than their database backed behemoth cousins like Wordpress, Joomla and Drupal. The main difference is that webpages are actual disk files on the webserver, instead of records in a database. This has advantages of security and portability - If you have a copy of the files, then you have a backup of the website. If you need to transfer your website to other, similar hosting - transfer the files and that&#39;s basically it.</p>

<p class="single">Mara CMS isn&#39;t a &#39;website builder&#39; though. Unlike such &#39;beginner grade&#39; products it doesn&#39;t have to be on special hosting, and you are not restricted to a supplied set of layouts and styles.&nbsp; Basically, any layout or styling you can create with a website, can be done with Mara CMS. No cloning jobs here, just original creativity.</p>

