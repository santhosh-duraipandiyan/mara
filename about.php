<h1>Version 7.2 :: Production release</h1>

<p>Version 7.x has been a while in development and has some quite fundamental changes. Please test before upgrading any live Mara site using 6.x or before. We think you&#39;ll be amazed by some of the new features in this release. Especially, the ability to to compose entire pages containing text, graphics, sound and video right on your own computer <em>before</em> you upload anything to your webhost.</p>

<div class="vcol">
<h3>Drag and drop</h3>

<p>V7 sees a paradigm shift towards doing most things by drag and drop. Or, if you prefer, by copy and paste.<br />
<br />
Following the principle of editing the actual page and not a copy thereof, items can be dropped or pasted directly into the place where you want them, and without any intermediate steps.<br />
<br />
The capability isn&#39;t limited to photos either - Try dropping or pasting PDF&#39;s, wordprocessor documents, text files, video, sound files, even other webpages. The system will choose the most appropriate action, be it displaying the item in the page or providing a hyperlink to it.</p>

<h3>Pending uploads</h3>

<p>Thanks to this feature, a page can now be constructed, images and words, entirely on your local computer <em>without uploading anything</em>. This allows for much easier and quicker experimentation with styles and layouts than would be possible where media has to be uploaded first. Now you can compose and test your ideas without wasting time and bandwidth on uploading material that you might decide not to use anyway.<br />
<br />
When you have a design you are happy with, just hit Save and the whole set of files is uploaded.&nbsp;</p>

<h3>Direct paste from photo editors</h3>

<p>Instead of dropping or pasting files, you can open a photo in a program such as Photoshop or Gimp, select the area you want, and press Control-C. Then go to the browser, position the caret where you would like your image, and press Control-V.<br />
<br />
The selected area of the photo will be pasted into your webpage, without ever needing to save it as a file. This avoids all the intermediate steps. The difference in speed and convenience has to be seen to be appreciated.&nbsp;</p>
</div>

<div class="vcol">
<h3>Video support</h3>

<p>Mara now provides three ways of inserting video content into your pages. There&#39;s the CKEditor media plugin, which accepts URLs from a number of services. If the video is on YouTube though, you can just drop the link straight into the page. Or, you can drag a local .webm file into your page, in which case it will become a (playable) pending upload, as happens with local photos dropped into your page.</p>

<h3>Chunked uploading</h3>

<p>This largely overcomes the size limits imposed by traditional uploading methods, and given a fast enough connection, makes it possible to send such items as fullscreen video files direct from the browser.</p>

<h3>Smart image sizing</h3>

<p>We&#39;ve had this for ages as a server-side feature, but thanks to extended capabilities of the latest browsers, we can now offer it pre-upload.<br />
<br />
Advantage is that you can now drop most kinds of photo or graphic straight into the page, and if it&#39;s too large to fit the page this will be sorted-out rightaway, before you make any adjustments, and most importantly, before you upload.</p>

<h3>Notes</h3>

<p>The user manual and help information are now all on the website, <a href="http://maracms.com" target="_blank">maracms.com</a> whilst this package consists purely of the framework on which to build a Mara website.&nbsp;<br />
<br />
We feel this is a better approach than bundling the help info as it provides you with a cleaner start to your website. Also, it allows for the help info to be updated regularly without having to replace this download.</p>

